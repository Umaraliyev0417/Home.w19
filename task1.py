class Calculator:
    def add(self, n1, n2):
        print(n1 + n2)
    def subtract(self, n1, n2):
        print(n1 - n2)
    def multiply(self, n1, n2):
        print(n1 * n2)
    def divide(self, n1, n2):
        print(n1 / n2)
calculator = Calculator()
calculator.add(10, 5)
calculator.subtract(10, 5)
calculator.multiply(10, 5)
calculator.divide(10, 5)
